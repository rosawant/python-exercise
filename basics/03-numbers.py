print(10 + 5) # 15
print(10 - 5) # 5
print(10 * 5) # 50
print(10 / 5) # 2.0
print(10 // 5) # 2
print(10 % 5) # 0
print(10 % 3) # 1 # modulus operator gives the remainder of the division
print(10 ** 5) # 100000

x = 10
x += 5 # x = x + 5 # can use all the operators like +=, -=, *=, /=, //=, %=, **=

round(2.3) # 2 # round the number to the nearest integer
round(3.14159, 2) # 3.14 # round the number to 2 decimal places
abs(-5) # 5 # absolute value of the number
## to import math module for more mathematical functions
import math
math.sqrt(16) # 4.0 # square root of the number
math.pow(2, 3) # 8.0 # 2 raised to the power of 3
math.pi # 3.141592653589793 # value of pi
math.e # 2.718281828459045 # value of e

