# Comparison Operators
10 > 3 # True
10 < 3 # False
10 == 3 # False
10 != 3 # True

# String comparison
## Each string has its own ASCII value for each character. The comparison is done based on the ASCII values of the characters in the strings.
"apple" > "banana" # False
"apple" < "banana" # True
"apple" == "apple" # True
"apple" == "Apple" # False
"apple" != "banana" # True
